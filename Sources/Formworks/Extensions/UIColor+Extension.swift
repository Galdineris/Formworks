import UIKit

extension UIColor {
    class var fwComponentBackground: UIColor {
        return UIColor(red: 240.0/255.0, green: 240.0/255.0, blue: 240.0/255.0, alpha: 1.0)
    }
    
    class var fwComponentShadow: UIColor {
        return .black
    }
    
    class var fwFormBackground: UIColor {
        return UIColor(red: 253.0/255.0, green: 253.0/255.0, blue: 253.0/255.0, alpha: 1.0)
    }
}
